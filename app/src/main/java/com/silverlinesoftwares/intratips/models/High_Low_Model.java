package com.silverlinesoftwares.intratips.models;

public class High_Low_Model {
    String symbol;
    String symbolDesc;
    String value;
    String year;
    String ltp;
    String value_old;
    String dt;
    String prev;
    String change;
    String pChange;

    public String getSymbol() {
        return symbol;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }

    public String getSymbolDesc() {
        return symbolDesc;
    }

    public void setSymbolDesc(String symbolDesc) {
        this.symbolDesc = symbolDesc;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getLtp() {
        return ltp;
    }

    public void setLtp(String ltp) {
        this.ltp = ltp;
    }

    public String getValue_old() {
        return value_old;
    }

    public void setValue_old(String value_old) {
        this.value_old = value_old;
    }

    public String getDt() {
        return dt;
    }

    public void setDt(String dt) {
        this.dt = dt;
    }

    public String getPrev() {
        return prev;
    }

    public void setPrev(String prev) {
        this.prev = prev;
    }

    public String getChange() {
        return change;
    }

    public void setChange(String change) {
        this.change = change;
    }

    public String getpChange() {
        return pChange;
    }

    public void setpChange(String pChange) {
        this.pChange = pChange;
    }
}
